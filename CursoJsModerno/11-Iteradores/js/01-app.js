// For loop 

// Se ejecuta desde 0, hasta que i sea menor a 10
for (let i = 0 ; i < 10 ; i++) {
    console.log(`Nuestro numero es ${i}`)
}

for (let i = 0 ; i < 10 ; i++) {
    if( i % 2 === 0 ) { // Recordar que con % sacamos el residuo para saber si es par o impar
        console.log("El numero es par")
    } else {
        console.log("Es impar")
    }
}

// Esta es la mejor manera para recorrer arreglos e ir mostrando su posicion

